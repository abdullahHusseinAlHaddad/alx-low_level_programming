#include "3-calc.h"

/**
 * op_add - the sum of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: sum of a and b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: sub of a and b.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: mul of a and b.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the division of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: quotient of a and b.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the remainder of the division of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the modul.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
